bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
            /* mod state */
        case KC_LGUI:
        case KC_RGUI:
            gui_held = record->event.pressed;
            break;
        case KC_RSFT:
        case KC_LSFT:
            shift_held = record->event.pressed;
            held_shift = keycode;
            break;
        case KC_LALT:
        case KC_RALT:
            alt_held = record->event.pressed;
            break;

            /* KEYBOARD PET STATUS START */

        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            ctrl_held = record->event.pressed;
            break;
        case KC_ESC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;

            /* KEYBOARD PET STATUS END */
    }
    return true;
}


